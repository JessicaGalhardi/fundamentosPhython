def add(a,b):
  """ It add two numbers

      param a: First value
      type a: int,float
      param b: Second value
      type b: int,float
      return: int, float
  """
  return a + b

def sub(a,b):
  """ It subtract two numbers

      param a: First value
      type a: int,float
      param b: Second value
      type b: int,float
      return: int, float
  """
  return a - b